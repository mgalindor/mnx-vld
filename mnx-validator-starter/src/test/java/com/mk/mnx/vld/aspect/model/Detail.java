package com.mk.mnx.vld.aspect.model;

public class Detail {
	
	private String name;
	
	private Double age;
	
	private String schoolarDegree;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Double getAge() {
		return age;
	}

	public void setAge(Double age) {
		this.age = age;
	}

	public String getSchoolarDegree() {
		return schoolarDegree;
	}

	public void setSchoolarDegree(String schoolarDegree) {
		this.schoolarDegree = schoolarDegree;
	}
	
	

}

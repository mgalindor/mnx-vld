package com.mk.mnx.vld.aspect;

public class Fly {

	private String name ;
	
	private Pilot pilot;
	
	private Double time;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Pilot getPilot() {
		return pilot;
	}

	public void setPilot(Pilot pilot) {
		this.pilot = pilot;
	}

	public Double getTime() {
		return time;
	}

	public void setTime(Double time) {
		this.time = time;
	}
	
	
}


